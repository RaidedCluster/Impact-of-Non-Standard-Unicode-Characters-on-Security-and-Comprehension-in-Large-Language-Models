def katakana_to_enclosed(katakana_str):
    katakana_to_enclosed_map = {
        'ア': '㋐', 'イ': '㋑', 'ウ': '㋒', 'エ': '㋓', 'オ': '㋔',
        'カ': '㋕', 'キ': '㋖', 'ク': '㋗', 'ケ': '㋘', 'コ': '㋙',
        'サ': '㋚', 'シ': '㋛', 'ス': '㋜', 'セ': '㋝', 'ソ': '㋞',
        'タ': '㋟', 'チ': '㋠', 'ツ': '㋡', 'テ': '㋢', 'ト': '㋣',
        'ナ': '㋤', 'ニ': '㋥', 'ヌ': '㋦', 'ネ': '㋧', 'ノ': '㋨',
        'ハ': '㋩', 'ヒ': '㋪', 'フ': '㋫', 'ヘ': '㋬', 'ホ': '㋭',
        'マ': '㋮', 'ミ': '㋯', 'ム': '㋰', 'メ': '㋱', 'モ': '㋲',
        'ヤ': '㋳', 'ユ': '㋴', 'ヨ': '㋵', 'ラ': '㋶', 'リ': '㋷',
        'ル': '㋸', 'レ': '㋹', 'ロ': '㋺', 'ワ': '㋻', 'ヰ': '㋼',
        'ヱ': '㋽', 'ヲ': '㋾',
    }
    return ''.join(katakana_to_enclosed_map.get(char, char) for char in katakana_str)

katakana_str = """カニュ・スピッコ・ライコ・ティズ?
"""
enclosed_str = katakana_to_enclosed(katakana_str)
print(enclosed_str)
